def load_quantized_model():
    """Load the INT8 quantized model"""
    print("Loading quantized model...")

    # Check if model exists
    if not os.path.exists(MODEL_PATH):
        print(f"ERROR: Model not found at {MODEL_PATH}")
        print("Please run simple_experiment.py first to generate the model.")
        return None

    model = torch.load(MODEL_PATH)
    model.eval()
    print(f"✓ Model loaded: {sum(p.numel() for p in model.parameters())} parameters")
    return model

def extract_weights_and_structure(model):
    """Extract quantized weights and model structure"""
    print("\nExtracting model weights and structure...")

    weights_data = {}
    layer_info = []

    for name, param in model.named_parameters():
        if param.requires_grad or True:  # Get all parameters
            # Convert to numpy
            weight_np = param.detach().cpu().numpy()
            weights_data[name] = weight_np

            layer_info.append({
                'name': name,
                'shape': list(weight_np.shape),
                'dtype': str(weight_np.dtype),
                'size': weight_np.size
            })

            print(f"  {name}: {weight_np.shape} ({weight_np.dtype})")

    return weights_data, layer_info

