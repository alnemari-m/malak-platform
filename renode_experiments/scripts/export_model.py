def save_model_metadata(layer_info):
    """Save model metadata as JSON"""
    metadata = {
        'model_architecture': 'MobileNetV2',
        'quantization': 'INT8 QAT',
        'dataset': 'CIFAR-10',
        'num_classes': 10,
        'input_shape': [3, 32, 32],
        'layers': layer_info,
        'total_parameters': sum(info['size'] for info in layer_info)
    }

    metadata_path = os.path.join(OUTPUT_DIR, 'model_metadata.json')
    with open(metadata_path, 'w') as f:
        json.dump(metadata, f, indent=2)

    print(f"✓ Metadata saved to {metadata_path}")

def main():
    print("=" * 60)
    print("PyTorch to C Model Export for Renode Simulation")
    print("=" * 60)
    print()

    # Create output directory
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # Load model
    model = load_quantized_model()
    if model is None:
        print("\nNOTE: Since the quantized model doesn't exist yet,")
        print("generating placeholder C code for proof-of-concept.")
        print("Run simple_experiment.py first for full export.")
        weights_data = {}
        layer_info = []
    else:
        # Extract weights
        weights_data, layer_info = extract_weights_and_structure(model)

        # Save metadata
        save_model_metadata(layer_info)

    # Generate C files
    header_content = generate_c_header(weights_data, layer_info)
    impl_content = generate_c_implementation(weights_data)
    main_content = generate_test_main()
    makefile_content = generate_makefile()

    # Write files
    header_path = os.path.join(OUTPUT_DIR, 'cifar10_model.h')
    impl_path = os.path.join(OUTPUT_DIR, 'cifar10_model.c')
    main_path = os.path.join(OUTPUT_DIR, 'main.c')
    makefile_path = os.path.join(OUTPUT_DIR, 'Makefile')

    with open(header_path, 'w') as f:
        f.write(header_content)
    print(f"✓ Generated {header_path}")

    with open(impl_path, 'w') as f:
        f.write(impl_content)
    print(f"✓ Generated {impl_path}")

    with open(main_path, 'w') as f:
        f.write(main_content)
    print(f"✓ Generated {main_path}")

    with open(makefile_path, 'w') as f:
        f.write(makefile_content)
    print(f"✓ Generated {makefile_path}")

    print("\n" + "=" * 60)
    print("✓ Model export complete!")
    print("=" * 60)
    print("\nGenerated files:")
    print(f"  - {header_path}")
    print(f"  - {impl_path}")
    print(f"  - {main_path}")
    print(f"  - {makefile_path}")
    print("\nNext steps:")
    print("  1. cd ../models")
    print("  2. make              # Build for ARM Cortex-M7")
    print("  3. cd ../simulations")
    print("  4. ./run_stm32h7.sh  # Run Renode simulation")
    print()

if __name__ == "__main__":
    main()
